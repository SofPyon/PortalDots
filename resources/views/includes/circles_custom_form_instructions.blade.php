<ul>
    <li>企画参加登録フォームを{{ config('app.name') }}上に設置できます</li>
    <li>企画参加登録フォームでは、企画名や団体名の入力欄のほか、<strong>独自の入力欄を設置</strong>できます</li>
    <li>フォームで集めた企画情報は<strong>CSVファイルとしてダウンロード</strong>できます</li>
    <li>企画参加登録の提出の要件として、<strong>学園祭係(副責任者)の登録を義務付ける</strong>ことができます</li>
    <li>メールアドレスの認証が未完了のユーザーは企画参加登録を行うことができないため、<strong>不正な企画参加登録を防ぐ</strong>ことができます</li>
    <li><strong>提出された企画参加登録は「確認中」状態となります。</strong>スタッフが参加登録を「受理」することで、その企画は{{ config('app.name') }}上の各種申請に回答できるようになります
    </li>
</ul>
